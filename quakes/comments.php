<center><h2><a name="author"></a>Feedback from the Author&nbsp;</h2></center>
<i>"The theme of "Cry the Beloved Mind: A Voyage of Hope" is practical
education about medications for brain conditions through fascination. I
added spice by confronting controversial medical dilemmas and important
social issues. <b>Ultimately realistic hope for recovery, respect for the
individual and communication of basic and special insights in neurology
and psychiatry override all else.</b> I wrote the book specifically to
ensure readability for patients, family members and general readers, but
I have been pleasantly surprised at the positive feedback that physicians,
psychologists, nurses and students in several areas have given. Cry the
Beloved Mind defines for the first time the new literary style of sciction-science
through literature. It is a voyage of respect that uses dialogue to solve
medical mysteries about brain medications."&nbsp;</i>
<blockquote>Vernon M Neppe, MD, PhD, Author.&nbsp;</blockquote>

