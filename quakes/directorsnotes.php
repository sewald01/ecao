<p>Our staged play reading of 2004 allowed significant insights into my developing ideas of how this play can be interpreted. The major challenge, as I see it, is for the director to get to the core of the characters. Even more than in most plays, the actors are not one dimensional, but living people experiencing significant personality development over time. This play requires three experienced actors with nuancing of roles, because of the characters’ profound life-changes. The actors need to work to make the characters really live and grow. If that doesn’t happen, this play will not succeed. If the actors breathe their characters, the educational, caring and dramatic experience will all come through. </p>
<p>The Doctor identifies universally, with the special qualities of the good physician. He demonstrates his dedication, learning and passion. Yet, while representing the universal ideals of medicine he is unusual in his special skills as both the empathic, creative psychiatrist and the caring, incisive neurologist. He speaks slowly and deliberately with great intent and meaning. He teaches, but not didactically, instead by interaction, as if talking to his patients, and he has a special relationship with his audience. He uses, inter alia, variation in his pace of speech as a communication means. The preconceived idea of the perfect character must not be there, and yet he has exceptional characteristics. Particularly important are his direct interchanges with the audience: He must engage them, and especially here his carefully chosen characterization shows. The affective information transmitted profoundly modifies his tone: There are secrets he shares, expressions of limitation, and of caring.
Wendy is the most vivacious, and most complex character; her varying vignettes give added spice to the play.
Lucy is Wendy’s contrast, an intelligent, communicative lady who is commonly thinking about how to improve herself. By her calm pursuit of health, she is the quietest of the three, but she must remain a real living being. </p>
<p>The playwright, Dr Neppe, in earlier versions, kept instructions to a minimum. These have been markedly accentuated in this latest version. They are only suggestions, however, and the director should still feel he/ she has free reign. For example in the script: There is a spelling out of silence:… for short pauses, ……for longer ones, and the statement `”pause” in the text, at times, to accentuate that even more. </p>
<p>These are some examples in the play of what is malleable for directors: <br><br>
Actual slides or video could enhance the “show and tell” aspects. This could clarify more complex information like EEGs; and fun spellings like “floccinaucinihilipilification”. Alternatively, the way the characters are portrayed could allow the audiences their own imagination and visual aids would then be seen as a distraction. However, if slides are used, the beam should be covered between projections of (say) twenty seconds. This prevents the actors being blinded by the light.
Another option is a musical score between scene changes. <br><br>
The headers in each scene in the play create a special style itself. This includes varying the seating and the movements, as well as the specific instructions given each actor. <br><br>
Phone conversations can involve real rings on cell phones and ostensible speakerphones with voices coming from e.g. the director. The husbands, Martin and Richard are deliberately not developed as full characters so as not to dilute the three protagonists in the play. Consequently their not appearing physically on stage is deliberate.<br><br>
The use of a “Voice Over” during a Lucy seizure and Wendy’s portrayal of her several previous psychiatric experience enhances the dramatic quality. These consolidate the personalities. Voice-overs can be pre-recorded and Lucy’s voice over and Wendy’s middle previous psychiatrist should be female.<br><br>
The audience response to the initial Farce scene has been gratifying, however, the purist director can eliminate it. If used, it can be played by the three characters, Wendy, Lucy and Doctor, using different roles and again with a voice over.
Even in play-readings, certain basic movements should be acted out and simple dress changes will enhance the acting. Varying the positions and dress of the Doctor narrator can increase the movement.
