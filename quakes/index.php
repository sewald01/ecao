<P>"Quakes"</P>


<P>A suicidal mother, and a computer specialist whose epileptic seizures
predict earthquakes, are the neuropsychiatrist’s patients in this brilliantly conceived three-person play of hope and medical dilemmas written in the new literary genre of sciction (science through literature). </P>

<P>Playwright Vernon M. Neppe MD, PhD  is an  internationally recognized
psychiatrist,  psychopharmacologist and neuropsychiatrist.    Director of the Pacific Neuropsychiatric Institute in Seattle, and author of more than 300 publications,  his books include Cry the Beloved Mind: A Voyage of Hope,   Innovative Psychopharmacotherapy and The Psychology of Déjà Vu and three 2006 follow up books on Déjà vu, namely, Déjà Vu; A Second Look, Déjà Vu Revisited and Déjà Vu: Glossary and Library,  and a monograph How Attorneys Can Best Utilize Their Medical Expert Witness: A Medical Expert’s Perspective. </P>

<P>In addition to listings  in America’s Top Doctors and  America’s Top
Psychiatrists, Professor Neppe is listed  in  Who’s Who in the World and in Americas Top Doctors. His other listings include Best Doctors in America,   500 Leaders of Influence ,   2000 Notable American Men,  5000 Personalities of the World, Great Minds of the 21st Century and International Man of the Year. </P>

<P>Educated in South Africa and at Cornell University, he has lectured
around the world in medicine, pharmacology, psychiatry and anomalistic psychology and led the first  US Delegation  in Delegation in Psychopharmacology and Neuropsychiatry. (People to People Ambassador Program to China in June 2006). </P>


<P>Quakes is informative, combining science and literature. In fact, this is the first play in Neppe's genre of sciction,  inspiring patients, families, laypersons and professionals.  The play is based on actual cases, and the education based on composite patients. </P>

<P>The world premiere of a play-reading of Quakes took place at the University of Hawaii in Honolulu on  Sunday May 30th 2004. Since then the play has been revised substantially to improve it even more. </P>
