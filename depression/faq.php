<h4>Frequently Asked Questions on Depression</h4>

<a name="depression_common"></a>
<strong>Is depression more common in women than men?</strong><br><br>
  
Indeed: Twice as many women have major depression than men. This is much mores so round 
about the time of the menopause.<br><br>

<a name="depression_age"></a>
<strong>At what age does depression begin?</strong><br><br> 

Any age. But particularly in the young. There is a sharp increase around the menopause 
in women. But pregnancy may improve, worsen or not effect depression.<br><br>

<a name="depression_gender"></a>
<strong>Are there gender differences in symptoms of depression?</strong><br><br> 

Yes. Women have more:
<ul>
<li>Over-eating, weight gain and over-sleeping</li>
<li>Anxiety</li>
<li>Eating disorders</li>
<li>Stressful life links to their illness</li>
<li>Effects of the seasons</li>
<li>Suicide attempts</li>
<li>Risk to become chronic and have recurrent illness</li>
</ul> 
Men have more:
<ul>
<li>Alcoholism and substance abuse</li>
<li>Completed suicides may be greater</li>
</ul>
(Kornstein SG, et al. Psychopharmacol Bull. 2002;36(suppl 3):99-112).<br><br> 
