<h4>WHAT ARE THE KEY FEATURES OF DEPRESSION?</h4>
There are Thinking,  Emotional and Physical Signs<br>
<font size="3"><em>DR NEPPE'S DEPRESSION MNEMONICS</em></font><br><br>

<font size="3">COGNITIVE (THINKING) FEATURES OF DEPRESSION. === HINGES.</font>
<ul>
<li>Hopelessness and helplessness</li>
<li>Inadequacy sense</li>
<li>Negative thoughts-sense of nothingness</li>
<li>Guilt sense and self-blame</li>
<li>Esteem loss</li>
  <li>Suicidal thoughts</li>
</ul>

<font size="3">AFFECTIVE (EMOTIONAL) FEATURES OF DEPRESSION.=== PAWS.</font><br> 
<ul>
<li>Pleasure experience lost -- "anhedonia"</li>
<li>Agitation and anxiety (co-exists in most depression cases)</li>
<li>Weepiness</li>
<li>Sadness sense</li>
</ul>

<font size="3">COMMON BODILY (SYSTEMIC) FEATURES OF DEPRESSION === SHIP  FADS</font><br>  
These can go both ways; the depression may aggravate them or they may aggravate the 
depession. It is important to ensure that there is no underlying medical condition or 
drug or other related side-effect.
<ul>
<li>Sexual problems, apathy and menstrual problems</li>
<li>Headache</li>
<li>Intestinal  complaints (nausea, vomiting, constipation, diarrhea, gas)</li>
<li>Pain -- vague: chest, joint/limb, back, abdominal pain</li>
<li>Fatigue</li>
<li>Attention difficulty (concentration and memory impaired)</li>
<li>Dizziness</li>
<li>Sleep disturbance</li>
</ul>

