Dr Vernon Neppe's bound book, Innovative Psychopharmacotherapy, was originally published by one of the most well-known and largest medical publishers, Raven Press in New York in 1989. (ISBN 0-88167-490-7), 223 pages, bound, hard cover).
<br /><br />
The first printing sold out very quickly and Dr Neppe the following year produced the second edition, with only one chapter, Chapter 2, markedly changing. The second edition had a smaller publication run than the first.
<br /><br />
The book has long been unavailable though occasional copies occur at Amazon or Barnes and Noble at very expensive prices.
