<H1 CLASS="title" align="center">Information for the Media</H1>

<BR><br>

Vernon Neppe MD, PhD has produced a
literary classic involving a whole new literary genre
which he has called <A class="std" href="/ctbm/excerpts_prologue.php">Sciction</A>. 
For this reason alone, and not even
taking into account his international expertise and pioneering
discoveries that have changed the lives of millions of people,
the opportunity to interview him should not be missed.

<br><br>

His latest book, <I>Cry the Beloved Mind: A Voyage of Hope</I> represents a series of voyages
in the pharmacology of psychiatry and neurology, reflecting a
single message: there is help for the anguished patient. <I>People
can be helped</I>, provided we are aware of the exact biochemical
or electrical abnormalities involved and we have the appropriate
interventions to alleviate the problems.

<br><br>

Dr Vernon Neppe is a media
expert who can impact on audiences and adapt himself to audiences.
He is in great demand as a lecturer, as a public speaker, on radio,
television and in newspapers.

<br><br>

Dr. Neppe's expertise is very broad and extends to such medical
and ethical dilemmas as why not to commit suicide, the dilemma
of generic drugs, herbal remedies for the brain, problems of drug
interactions, how someone can have seizures before earthquakes,
to what is normal?

<br><br>

Besides anything, Dr. Neppe has made very major discoveries: <BR>
He pioneered anticonvulsant use in psychiatry -- now being used by millions, and he discovered what is still the key treatment -- which seems
to work! -- for the incurable condition of tardive dyskinesia, the movement disorder that changed the fate of thousands of psychotic patients.

