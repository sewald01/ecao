<div class="emphasis" align="center"><strong>ABOUT THE AUTHOR</strong></div>
<br>
Vernon Neppe, MD, PhD, FRSSAf, FFPsych, FRCPC, is a specialist in neuropsychiatry 
and psychopharmacology, as well as a forensic expert. He and his family live in 
Seattle, Washington, where he practices medicine as Director of the Pacific Neuropsychiatric 
Institute, and as Attending Physician at Northwest Hospital. Dr. Neppe also is 
adjunct Professor of Psychiatry and Human Behavior at St Louis University, St 
Louis, MO. Until 1992, he was full-time University of Washington faculty (where 
in 1986 he founded and directed the first Division of Neuropsychiatry in a Department 
of Psychiatry in the United States). During his prestigious career, he has received 
numerous board certifications, specialist qualifications and professional awards 
and he has published and lectured extensively round the world. He is in-demand 
as a speaker on brain and medicines, and he has a special skill in communicating 
with different audiences, <em>making him an ideal candidate for media interviews</em>. 
His expertise ranges from brain conditions like amnesia --- he was interviewed 
for TV in the famous Jody Roberts case, to aggression, unusual behaviors and seizures 
and through to psychiatric medications, generic substitution, gun control, normality 
and the paranormal. His deep empathy, special expertise and important discoveries 
form the foundation of this unique book directed towards general readers, patients 
and families, as well as students in psychology, medicine and related areas. <br>
