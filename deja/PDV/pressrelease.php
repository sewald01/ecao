<hr noshade><br><br>
<span class=allcaps>Seattle, 16th April, 2003:</span> &quot;The Second Coming 
of Oliver Sacks.&quot; This is one reader's portrayal of Dr. Vernon M. Neppe's 
latest book <I>Cry the Beloved Mind: A Voyage of Hope.</I> High praise, indeed, 
given that Dr. Sacks has achieved an international reputation with his books, 
movies and TV programs in the area of unusual, abnormal brain functioning. <br>
<br>
Vernon Neppe MD, PhD, in his own right an internationally respected
neuropsychiatrist, expert on brain medications, and author of
Innovative Psychopharmacotherapy and The Psychology of D&eacute;j&agrave;
Vu, agrees that there are similarities: Dr. Sacks's books and
TV series, he says, are about the brain and are meant for the
general reader. Similarly, <I>Cry the Beloved Mind</I> aims
at educating patients, their families, the public, students and
academia about the brain.
<br><br>
Dr. Neppe says that the first chapter of his book (entitled
<I>Awakenings Revisited)</I> may well be compared to the movie
<I>Awakenings</I>. That film was based on Dr. Sacks's account
of how patients were re-awakened after many years by a special
drug. But, he adds: &quot;<I>Cry the Beloved Mind</I>&nbsp; also
encompasses an extra dimension, as I share several <I>personal
new</I> discoveries with my readers as well as targeting hope
for recovery. My book is far more than just a wonderful way for
the general reader to be educated in areas such as depression,
seizures, psychosis, brain medications and social medical issues.
I wrote it with a far deeper purpose in mind. There are many patients
suffering out there and I want to help them.&quot;
<br><br>
<I>Cry the Beloved Mind</I> is, indeed, a compassionate, respectful,
self-help book which allows patients, their families and the general
reader to understand what their doctors are doing when using powerful
mind-modifying medications such as Prozac, BuSpar and Tegretol.
As for Dr. Neppe's consultations in the book, he has taken a fresh
approach, writing in a style that he describes as being &quot;a
play within prose.&quot; It is fundamentally a dialogue between
the generic 'Doctor' and his patients or medical students using
the entirely new literary genre of <I>sciction</I>, medical science
expressed through fictitious composite case histories. Although
incorporating various patients, all the cases are based on fact,
so that there is a real educational opportunity while patient
confidentiality is protected. The book functions as a unit with
the medical student, Andrew, developing not only skills but medical
maturity as the book progresses. Dr Neppe emphasizes that despite
the new name of &quot;sciction&quot;, the book is still classified
under non-fiction. &quot;Just because it is written as a literary
piece, with dialogue and characterization, does not diminish the
fundamental factual science in the book. The new literary label
could as easily have been called scientific text education through
literature. This was the challenge of the book: to maintain science,
to educate and to fascinate.&quot;
<br><br>
The section of Dr. Neppe's book that some may find most intriguing
are the pages devoted to so-called 'medical social issues'. Embedded
in every chapter, are short, spicy interchanges about the use
of generic medications, relating to issues surrounding legitimate
informed consent, as well as what constitutes normality, self-growth,
and the ongoing dilemmas of treatment. Also included are reasons
why not to commit suicide.
<br><br>
For the reader, a survey of the Table of Contents of <I>Cry
the Beloved Mind: A Voyage of Hope </I>is most illuminating. Each
of the twelve chapters build to make a magnificent whole. For
instance.
<br><br>
<UL>
  <LI><I>The woman who predicts earthquakes</I> provides the fascination
  for the education about seizure advances.
  <LI><I>Curing the incurable</I> describes Dr. Neppe's amazing
  discovery of the first apparent cure for the terrible movement
  disorder of tardive dyskinesia, one of the major scourges in
  all of psychiatry.
  <LI><I>Tomorrow I will kill myself</I> changes when a patient's
  atypical brain firing is finally treated successfully.
  <LI><I>The deep dark pit</I> illustrates how Dr. Neppe was able
  to help even the most resistant depressed patient.
  <LI>On the lighter side, two chapters are paired together: <I>Sugar
  and spice </I>(discussing such social drugs as coffee), and also
  <I>And everything nice</I>(focusing on herbal remedies).
</UL>
<br><br>
And yet, the most valuable reference portion of the book may
be the comprehensive glossaries: patients and families repeatedly
comment on how useful these are to their education; so do students.
<br><br>
Dr. Michael Norden, author of <I>Beyond Prozac</I>, is impressed
with Dr. Neppe's new approach. It is, he says, &quot;A compelling
first-hand experience of the fascinating world of neuropsychiatry.
Witnessing this master clinician at work fuels a realistic optimism
about recovery from even the most extreme afflictions of the mind.&quot;
