What sort of a phenomenon was Lorenz Hart thinking of in 1937 when he wrote those still popular verses for the Rodgers and Hart production Babes in Arms ?
<br><br>
Was Hart hitting the single note of an emotion or an illusion unique to himself or to the poets, artists, or young lovers of his era or was he, in reality, striking a harmonious chord in the lives of millions of people from all fields of endeavour, in all epochs and of all ages who had experienced at one time or another a strange and wonderful feeling that “...some things that happen for the first time, Seem to be happening again”?

