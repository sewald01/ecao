<h3>More Information on Cry the Beloved Mind</h3>

<p>The chapters form a series of linked stories blending several real patients together into one using the new style of sciction -- science through literature. Through these fictitious case histories, Dr. Neppe explores how correction of the underlying biology of the brain can do wonders for one's mind. The use of extensive dialogue simplifies complex areas and allows easier targeting of specific areas of the book such as depression and anxiety, seizures, psychoses and movement disorders. Dr. Neppe provides insights into drug interactions as well as fashionable alternative medicines like St John's wort.</p>

