<p>
<b>Let Us Imagine</b><br />
We've already used a fun and simple "imagine" metaphor in Chapter 1: an elephant discovering his trunk. And we used that to demonstrate the hidden components of Space, Time and Consciousness, plus freedom of choice. Let's see how imagination has been used more seriously, because when we talk of not so obvious elements in reality, we need to apply our creativity skills.
<br /><br />

Imagination played an important role in Einstein's work. His classic 1905 paper on the Special Theory Of Relativity reflected a simple thought experiment: he imagined the electrodynamic interaction between a moving magnet compared with a stationary conductor, and then reversed the situation to illustrate his theoretical assumption of no preferred reference frame. 31 
<br /><br />

The use of imaginative thought experiments also played a major role in his later classic paper on the General Theory of Relativity. Ultimately, proof of this principle of "no preferred reference frame" was achieved by explaining the observed departure of the orbit of the planet Mercury compared with that predicted by Newtonian physics.32; 33 So these were examples of Einstein's famous thought experiments.
<br /><br />

Similarly, we could present thought experiments pointing out the limitations of the usual standard paradigmatic physicalist conventional materialist model relating to three dimensions of space and one point in time (3S-1t) that we humans experience on earth on a day-to-day basis at this time (our "sentient reality"). 
<br /><br />

One limitation is a lack of vocabulary because we're describing events or ideas that are outside the conventional understanding of the world. Moreover, the conclusions suggested are tentative, and sometimes several steps ahead. When that happens, it's like solving a mathematical problem by avoiding several of the intermediate stages and locating just the final result. This becomes a challenge that may be correct, but each stage should best be justified. We therefore do this as a tentative and imaginary exercise. We note that much of this still requires demonstrating, and other results of those final stages may produce conclusions that could be interpreted in multiple ways.
<br /><br />

&bull;&nbsp;Let us imagine a model where space, time, and consciousness (STC) all exist.
<br /><br />
&bull;&nbsp;Moreover, let us imagine a model in which the dimensions of all of Space, Time and a broader "Consciousness" are tethered together inseparably at a specific point, line or volume. This means they are linked ranging from tightly or loosely. But none of S, T and C cannot ever not be totally separated and unlinked: They're always "talking to each other" even if the linkage occurs across different dimensions. 
<br /><br />
&bull;&nbsp;Let's conceptualize this tethering as frequently partially released, altering this inseparability with each component protruding separately, although there is always the tethering in at least one point. But the tethering remains loose or tight across dimensions.
<br /><br />
&bull;&nbsp;Let us imagine a model where despite data on the future, we can still include free-will. Effectively, we're imagining choices of direction for the future.
<br /><br />
&bull;&nbsp;Let us imagine a model where time in the present is experienced not just as one moment in time but where we can move to the past or the future. 
<br /><br />
&bull;&nbsp;Let us imagine a model with extra Dimensions that could explain the existence of an extent of Space and Time at a far broader level, but where it still always and necessarily incorporates tethering with meaningful information. 
<br /><br />
&bull;&nbsp;Let us imagine a model where the content is mass, energy and "consciousness content" and that when this requires measurement it would be through its extent of Space, Time and "Consciousness extent" (a simple example of "extent" is measurement the length, breadth and height in Space; this varies with each example, meaning variables of extent).
<br /><br />
&bull;&nbsp;Let us imagine a model where there are several more dimensions in addition to our conventional sentient reality dimensions that we are directly experiencing, like 3S-1t. 
<br /><br />
&bull;&nbsp;Let us imagine a model where these "higher" or different dimensions allow one to more easily conceptualize these lower dimensional data because effectively one is seeing them top-down from "outside the box".
<br /><br />
&bull;&nbsp;Let us imagine a model where infinity becomes an essential part of Reality and is inseparably linked with the finite subreality.
<br /><br />
&bull;&nbsp;Let us imagine a model where we can explain "order" and how life comes about.
If one had such a model, what evidence would be useful to demonstrate it? 
<br /><br />

The above describes the key principles of our model of TDVP based on the axiom that all of "Space, Time and Consciousness are inseparably tethered together from their origin".

